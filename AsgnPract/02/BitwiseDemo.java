class BitwiseDemo{

	public static void main( String[] args){
		int n1 = 5;
		int n2 = 3;
		System.out.println(n1 & n2);
		System.out.println(n1 | n2 );
		System.out.println(n1 ^ n2 );
		System.out.println(n1 << 1 );
		System.out.println(n1 << 3 );
		System.out.println(n1 >> 1 );
		System.out.println(n1 >> 3 );
	}
}
